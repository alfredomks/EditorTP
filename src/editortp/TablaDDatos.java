    public double[] getXseleccionados(){
        double x[]= new double [getNumeroDfilasActivadas()];
        int j=0;
        for (int i = 0; i < filas.size(); i++) {
            if(Boolean.parseBoolean(filas.get(i).get(0).toString())==true){
                x[j]=Double.parseDouble(filas.get(i).get(2).toString());
                j++;
            }
        }

        return x;
    }
    public double[][] getFXseleccionados(){
        double x[][]= new double [getNumeroDfilasActivadas()][1];
        int j=0;
        for (int i = 0; i < filas.size(); i++) {
            if(Boolean.parseBoolean(filas.get(i).get(0).toString())==true){
                x[j][0]=Double.parseDouble(filas.get(i).get(3).toString());
                j++;
            }
        }
        return x;
    }
    public  int getNumeroDfilasActivadas(){
        int aux=0;
        for (int i = 0; i < filas.size(); i++) {
            if(Boolean.parseBoolean(filas.get(i).get(0).toString())==true)
                aux++;
        }
        return aux;
    }

    public int getFilaSeleccionada(){
        int x=-1;

         for (int i = 0; i < filas.size(); i++) {
            if(Boolean.parseBoolean(filas.get(i).get(0).toString())==true){
                  x=i;
                  break;
            }
        }

        return x;
    }
